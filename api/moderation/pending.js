// Vercel Serverless Function
import { getPendingSubmissions, loadSubmissions, getStorageInfo } from './shared-storage.js';

export default async function handler(req, res) {
  // –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º CORS –∑–∞–≥–æ–ª–æ–≤–∫–∏
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'GET, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type, Authorization');

  if (req.method === 'OPTIONS') {
    return res.status(200).end();
  }

  if (req.method !== 'GET') {
    return res.status(405).json({ message: 'Method not allowed' });
  }

  try {
    // –ü—Ä–æ–≤–µ—Ä–∫–∞ –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏ –∞–¥–º–∏–Ω–∞
    const authHeader = req.headers.authorization;
    if (!authHeader || !authHeader.startsWith('Bearer ')) {
      return res.status(401).json({ message: '–¢—Ä–µ–±—É–µ—Ç—Å—è –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏—è' });
    }

    const token = authHeader.substring(7);
    let user;
    try {
      const decoded = JSON.parse(Buffer.from(token, 'base64').toString());
      user = decoded;
    } catch (e) {
      return res.status(401).json({ message: '–ù–µ–≤–µ—Ä–Ω—ã–π —Ç–æ–∫–µ–Ω' });
    }

    // –ü—Ä–æ–≤–µ—Ä–∫–∞ —Ä–æ–ª–∏ –∞–¥–º–∏–Ω–∞
    if (user.role !== 'admin') {
      return res.status(403).json({ message: '–î–æ—Å—Ç—É–ø –∑–∞–ø—Ä–µ—â–µ–Ω. –¢—Ä–µ–±—É—é—Ç—Å—è –ø—Ä–∞–≤–∞ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞' });
    }

    // –ü–æ–ª—É—á–∞–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ —Ö—Ä–∞–Ω–∏–ª–∏—â–µ
    const storageInfo = getStorageInfo();
    console.log('üìä Storage info:', storageInfo);
    
    // –ó–∞–≥—Ä—É–∂–∞–µ–º –≤—Å–µ –∑–∞—è–≤–∫–∏
    const allSubmissions = loadSubmissions();
    console.log('üìã All submissions:', {
      total: allSubmissions.length,
      ids: allSubmissions.map(s => s.id),
      titles: allSubmissions.map(s => s.title),
      statuses: allSubmissions.map(s => s.status),
    });
    
    // –ó–∞–≥—Ä—É–∂–∞–µ–º –∑–∞—è–≤–∫–∏ –Ω–∞ –º–æ–¥–µ—Ä–∞—Ü–∏—é
    const pendingSubmissions = getPendingSubmissions();
    
    console.log('üìã Pending submissions result:', {
      count: pendingSubmissions.length,
      ids: pendingSubmissions.map(s => s.id),
      titles: pendingSubmissions.map(s => s.title),
    });
    
    // –ï—Å–ª–∏ –∑–∞—è–≤–æ–∫ –Ω–µ—Ç, –ª–æ–≥–∏—Ä—É–µ–º –¥–ª—è –æ—Ç–ª–∞–¥–∫–∏
    if (pendingSubmissions.length === 0 && allSubmissions.length === 0) {
      console.log('‚ö†Ô∏è No submissions found in storage at all');
      console.log('üí° This means:');
      console.log('   1. Either no submissions have been saved yet');
      console.log('   2. Or submissions are saved on a different Vercel instance');
      console.log('   3. Or storage is not persisting between function calls');
      console.log('üí° Solution: Use a database (PostgreSQL, MongoDB) or external storage service');
    } else if (pendingSubmissions.length === 0 && allSubmissions.length > 0) {
      console.log('‚ö†Ô∏è Submissions exist but none are pending');
      console.log('üí° All submissions have been processed (approved/rejected)');
    }

    return res.status(200).json({
      submissions: pendingSubmissions,
      count: pendingSubmissions.length,
    });
  } catch (error) {
    console.error('Error loading pending submissions:', error);
    return res.status(500).json({ message: '–û—à–∏–±–∫–∞ –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ –∑–∞—è–≤–æ–∫' });
  }
}

